// Title 生成器
function genTitleNew(c){
  var P=c.plt||'微信公众号',T=c.sty||'',L=c.len||'不限制',Cnt=c.cnt||'10个';
  var stys=T?T.split('、'):['数字列表型','问句悬念型','对比冲突型','情感共鸣型','干货价值型','热点借势型','故事叙述型'];
  var topic=c.extra||'个人成长与效率提升';
  var count=parseInt(Cnt)||10;

  var titles=[];
  for(var i=0;i<count;i++){
    var st=$(stys);
    var tmpl=$(TitleData.pools[st]||TitleData.pools['数字列表型']);
    var n=Math.floor(Math.random()*8+3);
    var title=tmpl.replace(/\{n\}/g,n).replace(/\{n2\}/g,n+Math.floor(Math.random()*3+2)).replace(/\{topic\}/g,topic);
    titles.push(title);
  }
  titles=[...new Set(titles)].slice(0,count);
  while(titles.length<count){
    var lastPools=TitleData.pools[stys[titles.length%stys.length]];
    titles.push($(lastPools).replace(/\{n\}/g,Math.floor(Math.random()*8+3)).replace(/\{topic\}/g,topic));
  }

  return c.header+'\n\n以下是为您生成的 '+titles.length+' 个'+P+'标题方案：\n\n'+titles.map(function(t,i){return(i+1)+'. '+t}).join('\n')+'\n\n——\n✦ 生成说明：共 '+titles.length+' 个标题，覆盖'+stys.join('、')+'等类型，适配'+P+'平台调性。\n'+(c.extra?'创作主题：'+c.extra:'主题：'+topic);
}

